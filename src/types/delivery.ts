/**
 * Единый контракт данных зоны доставки.
 * Используется: админка (зоны), корзина (селект района), страница «Доставка и оплата».
 * Источник истины: таблица delivery_zones в Supabase.
 */
export interface DeliveryZone {
  id: string;
  /** Название района */
  title: string;
  /** Стоимость доставки (руб) до порога бесплатной доставки */
  price: number;
  /** Условия / подрайоны (опционально) */
  conditions?: string | null;
  /** Порог суммы заказа, от которого доставка бесплатна (руб) */
  freeFrom: number;
  /** Порог «до X ₽» (руб), в описании тарифа */
  paidUpTo: number;
  /** Порядок сортировки */
  sortOrder: number;
}
